def french_phone_number?(phone_number)
  # TODO: true or false?
  if phone_number.match?(/(\(\+33\)|0|\+33|0033)[1-9]([0-9]{8}|([0-9\- ]){12})/)
    return true
  elsif phone_number.match?(/(\(\+33\)|0|\+3|0033)[1-9]([0-9]{9}|([0-9\- ]){14})/)
    return true
  else
    return false
  end
end

